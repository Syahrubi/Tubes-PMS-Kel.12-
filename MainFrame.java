    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        
        //if JTable already have data of txt file
        if(importTxtFileObj.tableHave == "fileData"){
            JOptionPane.showMessageDialog(this, "Please click remove all button");
        }
        
        
        else{
            // Model Name
            modelNamee = modelNameTextField.getText();
            // Color
            color = colorTextFiled1.getText();
            // type
            type = vechTitle.getText();
            // cateogary or company name 
            cateogary = cateogaryComboBox.getSelectedItem().toString();
           // fuel tank capacity
            fuleTankCapacity =fuleTankCapacityTextField.getText();
            // Price
            price = priceTextField.getText();
            
            //___set no.of gear none if vechicle type is Scooter__________//
            if(type.equals("Bikes")){
                // no. of Gears (Bike) 
                noOfGears = ComboBoxNoOfGears1.getSelectedItem().toString();
            }
            else if(type.equals("Scooter")){
                noOfGears ="None";
            }
            
            //_____________Range___________________//
            if (rangeRadioButton120.isSelected()) {
                range = 120;
            } else if (rangeRadioButton150.isSelected()) {
                range = 150;
            } else if (rangeRadioButton200.isSelected()) {
                range = 200;
            } else if (rangeRadioButton250.isSelected()) {
                range = 250;
            }

            //_____________Disc Option_________________/
            if (discOptionRadioButton1.isSelected()) {
                discOption = "Single Disc";
            } else if (discOptionRadioButton2.isSelected()) {
                discOption = "Double Disc";
            }

            //________________fuelDeliverySystem_____________//
            if (fuleDeliverySystemRadioButton1.isSelected()) {
                fuleDeliverySystem = "Carburetor";
            } else if (fuleDeliverySystemRadioButton2.isSelected()) {
                fuleDeliverySystem = "Fuel injection (FI)";
            }

            //_________________breakingSystem_____________//
            if (breakingSystemRadioButton1.isSelected()) {
                breakingSystem = "ABS";
            } else if (breakingSystemRadioButton2.isSelected()) {
                breakingSystem = "None ABS";
            }
            
            
            //____________ Check if user input is empty or not________________//
            //___________(checkEmpty is method of validation class)__________//
            isEmpty = validationObj.checkEmpty(modelNamee, color, type, range, discOption, fuleDeliverySystem, breakingSystem, fuleTankCapacity, price);
            
            if(!isEmpty.equals("no")){
                JOptionPane.showMessageDialog(this, "Please insert "+isEmpty+" !!!");
            }
            //__if not empty
            else if(isEmpty.equals("no")){
                
                //______Check if fuel tank and price is int_______________//
                //__(checkInteger is method of validation class)
                isInt = validationObj.checkInteger(fuleTankCapacity, price);
                
                if(!isInt.equals("no")){
                    JOptionPane.showMessageDialog(this, "Fuel Tank Capacity and Price(IDR.)" + "\n" + "can not be String!!!");
                }
                
                //if every thing is ok
                else if(isInt.equals("no")){
                    //_______Create Vechicle ID____________//
                    //__(createID method is in validation class)
                    vehicleId = validationObj.createId(type);
                    
                    //______add user data to array______//(validation class method) 
                    validationObj.insertIntoArray(type, cateogary,vehicleId, modelNamee,range, color, noOfGears, discOption,
                            fuleDeliverySystem, breakingSystem, fuleTankCapacity,price, orgArray);
                    
                    //__ adding array list in JTable__//(methos is in ApplicationInfo class)
                    appliancesInfoObj.addToTable(displayTable, orgArray);
                    
                    JOptionPane.showMessageDialog(this, "Registered Sucessfully !!!");
                }
            }
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

    
    
    
    
