    public void saveAllData(){
        //open file chooser
        JFileChooser saveFileChooser = new JFileChooser();
        // set file extension
        FileNameExtensionFilter filter = new FileNameExtensionFilter("TEXT FILES (.txt)", "txt", "text");
        saveFileChooser.setFileFilter(filter);
        // set file chooser title
        saveFileChooser.setDialogTitle("Specify a file to save");
        // checking if file is selected
        int userSelection = saveFileChooser.showSaveDialog(this);
         
        String dataTosave = SaveFileObj.save(saveFileChooser, userSelection, displayTable);
        if("saved".equals(dataTosave)){
            JOptionPane.showMessageDialog(this, "Saved in selected File !!!");
        }
           
    }
    
    
    
    
